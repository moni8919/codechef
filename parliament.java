import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sc=new Scanner(System.in);
		int t=sc.nextInt();
		while(t-->0){
		    byte n=sc.nextByte();
		    byte x=sc.nextByte();
		    System.out.println(x>=n/2.0 ? "YES" : "NO" );
		
		}
	}
}
	